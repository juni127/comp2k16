;Setar entradas e saidas
BANKSEL TRISA
CLRF TRISB
BANKSEL PORTA

CENTENA  EQU 0X1F
DEZENA   EQU 0X1E
UNIDADE  EQU 0X1D
NUMERO   EQU 0X1C
AUX      EQU 0X1B
RES      EQU 0X1A

;Programa principal
MAIN
CALL ENTRADA ; Ler dados das portas
MOVF NUMERO, 0
