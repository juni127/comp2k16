    private int getTipoIndex(Motocicleta.Tipo t){
        if(t == Motocicleta.Tipo.TRAIL)
            return 0;
        else if(t == Motocicleta.Tipo.STREET)
            return 1;
        else if(t == Motocicleta.Tipo.ESPORTIVA)
            return 2;
        else
            return 3;
    }
    
    private int getStatusIndex(Veiculo.Status s){
        if(s == Veiculo.Status.A_VENDA)
            return 0;
        else
            return 1;
    }
    
    private Motocicleta.Tipo getTipo(int index){
        switch(index){
            case 0:
                return Motocicleta.Tipo.TRAIL;
            case 1:
                return Motocicleta.Tipo.STREET;
            case 2:
                return Motocicleta.Tipo.ESPORTIVA;
            default:
                return Motocicleta.Tipo.CUSTOM;
        }
    }
    
    private Veiculo.Status getStatus(int index){
        switch(index){
            case 0:
                return Veiculo.Status.A_VENDA;
            default:
                return Veiculo.Status.VENDIDO;
        }
    }

