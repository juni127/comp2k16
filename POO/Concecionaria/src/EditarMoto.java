    private void criarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarButtonActionPerformed
        // TODO add your handling code here:
        //Verificar se tudo foi preenchido
        if(!modeloMotoField.getText().equals(moto.getModelo()))
            moto.setModelo(modeloMotoField.getText());
        if(!cilindradasField.getText().equals(Integer.toString(moto.getCilindradas())))
            moto.setCilindradas(Integer.parseInt(cilindradasField.getText()));
        if(!chassiField.getText().equals(Integer.toString(moto.getChassi())))
            moto.setChassi(Integer.parseInt(chassiField.getText()));
        if(!anoField.getText().equals(Integer.toString(moto.getAno())))
            moto.setAno(Integer.parseInt(anoField.getText()));
        if(!combField.getText().equals(moto.getComb()))
            moto.setComb(combField.getText());
        if(!pesoField.getText().equals(Double.toString(moto.getPeso()).replaceAll(".", ",")))
            moto.setPeso(Double.parseDouble(pesoField.getText().replaceAll(",", ".")));
        if(!kmField.getText().equals(Double.toString(moto.getKm()).replaceAll(".", ",")))
            moto.setKm(Double.parseDouble(kmField.getText().replaceAll(",", ".")));
        if(getStatus(statusMotoBox.getSelectedIndex()) != moto.getStatus())
            moto.setStatus(getStatus(statusMotoBox.getSelectedIndex()));
        if(getTipo(tipoMotoBox.getSelectedIndex()) != moto.getTipo())
            moto.setTipo(getTipo(tipoMotoBox.getSelectedIndex()));
            
        if(mainGerente != null)mainGerente.editMoto(moto, index);
        if(mainVendedor != null)mainVendedor.editMoto(moto, index);
        
        this.setVisible(false);
    }//GEN-LAST:event_criarButtonActionPerformed

