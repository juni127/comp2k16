    public NovaVenda(List<Veiculo> veiculos, List<Cliente> clientes, Funcionario funcionario, java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        
        this.veiculos = veiculos;
        this.clientes = clientes;
        this.funcionario = funcionario;
        this.pai = parent;
        
        vList.removeAllElements();
        cList.removeAllElements();
        for(Veiculo v : veiculos)
            vList.addElement(v.getModelo() + " " + v.getAno());
        for(Cliente c : clientes)
            cList.addElement(c.getNome());
        
        if(!veiculos.isEmpty())
            selectedVeiculo = 0;
        else
            selectedVeiculo = -1;
        
        if(!clientes.isEmpty())
            selectedCliente = 0;
        else
            selectedCliente = -1;
        
        if(clientes.isEmpty() || veiculos.isEmpty())
            vendidoButton.setEnabled(false);
        
        initComponents();
        
        veiculoBox.setModel(vList);
        clienteBox.setModel(cList);
        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        dataLabel.setText(dateFormat.format(date));
        
        pList.removeAllElements();
        int dp = clientes.get(selectedCliente).getDependentes();
        pList.addElement("A vista");
        if(dp >=1 )
            pList.addElement("12");
        if(dp >=2 )
            pList.addElement("24");
        if(dp >=3 )
            pList.addElement("36");
        if(dp >=4 )
            pList.addElement("48");
        
        this.setTitle("Nova venda");
        
        parcelasBox.setSelectedIndex(0);
        
    }

