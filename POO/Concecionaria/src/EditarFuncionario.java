    private void posFuncFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_posFuncFieldActionPerformed
        // TODO add your handling code here:
        int index  = posFuncField.getSelectedIndex();
        switch(index){
            case 0:
                anosFuncField.setEnabled(true);
                tempoLabel.setText("Tempo de trabalho:");
                grFuncField.setEnabled(true);
                break;
            case 1:
                anosFuncField.setEnabled(true);
                tempoLabel.setText("Anos de trabalho:");
                grFuncField.setEnabled(false);
                break;
            case 2:
                anosFuncField.setEnabled(false);
                grFuncField.setEnabled(false);
                break;
        }
    }//GEN-LAST:event_posFuncFieldActionPerformed

    private void criarFuncButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarFuncButtonActionPerformed
        // TODO add your handling code here:
        if(!userFuncField.getText().equals(func.getUsuario()))
            func.setUsuario(userFuncField.getText());
        if(!senhaFuncField.getText().equals(func.getSenha()))
            func.setSenha(senhaFuncField.getText());
        if(!rgFuncField.getText().equals(func.getDocumento()))
            func.setDocumento(rgFuncField.getText());
        if(!nascFuncField.getText().equals(func.getNasc().string()))
            func.setNasc(new Data(nascFuncField.getText()));
        if(!admFuncField.getText().equals(func.getAdmissao().string()))
            func.setAdmissao(new Data(admFuncField.getText()));
        if(!salarioFuncField.getText().equals(Double.toString(func.getSalario()).replaceAll(".", ",")))
            func.setSalario(Double.parseDouble(salarioFuncField.getText().replaceAll(",", ".")));
        if(!nomeFuncField.getText().equals(func.getNome()))
            func.setNome(nomeFuncField.getText());
        int option = posFuncField.getSelectedIndex();
        Funcionario funcionario;
        switch(option){
            case 0:
                VendedorJr junior = (VendedorJr)func;
                if(!anosFuncField.getText().equals(junior.getTempoTrabalho() + ""))
                    junior.setTempoTrabalho(Integer.parseInt(anosFuncField.getText()));
                mainGerente.editFuncionario(junior, this.index);
                break;
            case 1:
                VendedorS senior = (VendedorS)func;
                if(!anosFuncField.getText().equals(senior.getAnosTrabalho()+ ""))
                    senior.setAnosTrabalho(Integer.parseInt(anosFuncField.getText()));
                mainGerente.editFuncionario(senior, this.index);
                break;
            default:
                Gerente gerente = (Gerente)func;
                mainGerente.editFuncionario(gerente, this.index);                                
                break;
        }
        this.show(false);
    }//GEN-LAST:event_criarFuncButtonActionPerformed

